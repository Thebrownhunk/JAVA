public class variable {
    public static void main(String[] args){

        //VARIABLE
        //syntax:- dataType variableName = Value;

        //Rule's of nameing a variable:-
        //Name's can contain letter,digit,underscore,dollarsign.
        //Name's must begin with letter.
        //Name's should start with a lower case letter and can not contain white sapce.
        //Name's can also begin with $ and _ .
        //Name's are case sensitive.
        //Reserved word can not be used as name.

        int myNum = 10;
        System.out.println(myNum);

        //Declar Multiple Variable's:-
        int x = 10 , y = 20 , z = 30 ;
        System.out.println(x+y+z);
    }
}
